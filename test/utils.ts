const reverseStringEnum = <T extends Record<keyof T, string>>(e: T) =>
  Object.fromEntries(Object.entries(e).map(([k, v]) => [v, k])) as {
    [K in keyof T as T[K]]: K;
  };

const CountryReverseEnumFromCountryWithNoPostalCode = reverseStringEnum(
  CountryWithNoPostalCode
);
const CountryReverseEnumFromCountryWithPostalCode = reverseStringEnum(
  CountryWithPostalCode
);

export const castCountryWithNoPostalCode = <T extends CountryWithNoPostalCode>(
  t: T
) => Country[CountryReverseEnumFromCountryWithNoPostalCode[t]];

export const castCountryWithPostalCode = <T extends CountryWithPostalCode>(
  t: T
) => Country[CountryReverseEnumFromCountryWithPostalCode[t]];

